import math 
  
                      #Math Functions


print(round(3.9));         #4
print(abs(-1.45));         #1.45
print(math.fabs(-1))       #1.00       (converts number into positive float number)
print(math.floor(2.9));    #2
print(math.ceil(2.9));     #3
print(math.factorial(5))   # directly generates the factorial of 5

lst=[15,20,30,50]        # creating and initializing a list
print(math.fsum(lst))    # prints the sum(in float) of all the elements/items in a list (or a tuple)  

print(math.sqrt(16))      #4

